data = pd.read_csv('pyrad_bins.csv',sep=";")
data = data.iloc[:,1:] # remove index column

print(np.shape(data))

data_ = data.copy()
for i in range(len(data_)):
    for col in data.columns:
        data_.loc[i, col] = '{}{}'.format(str(col).split("_")[2], data_.loc[i,col]).strip()

