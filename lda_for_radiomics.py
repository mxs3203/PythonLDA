data_ = data_.values.tolist()

id2word = corpora.Dictionary(data_)

# Create Corpus
corpus = [id2word.doc2bow(text) for text in data_]

num_topics = 4
# Build LDA model
lda_model = gensim.models.LdaMulticore(corpus=corpus,
                                       id2word=id2word,
                                       num_topics=num_topics,
                                       passes=20,
                                       iterations=200,
                                       minimum_phi_value=0.01)

# Print the Keyword in the 10 topics
print("LDA")
